const somar = function(x, y){
    let resultado = x + y
return "Função Somar: " + x + " + " + y + " = " + resultado
}

const subtrair = function(x, y){
    let resultado = x - y
    return "Função Subtrair: " + x + " - " + y + " = " + resultado
}

console.log(somar(2, 5))
console.log(subtrair(10, 4))
